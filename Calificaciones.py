# Realiza un programa que pida 3 calificaciones y calcule el promedio 

Calificacion1 = int (input ("NUMERO DE LA PRIMERA CALIFICACION"))
Calificacion2 = int (input ("NUMERO DE LA SEGUNDA CALIFICACION"))
Calificacion3 = int (input ("NUMERO DE LA TERCERA CALIFICACION3"))

Promedio = (Calificacion1 + Calificacion2 + Calificacion3) / 3 

print ("El promedio de las calificaciones es:", Promedio) 
